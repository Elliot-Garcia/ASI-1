package com.model;

public class AccountDto {
		private String login;
		private String id;
		
		public AccountDto() {
		}

		public String getLogin() {
			return login;
		}

		public void setLogin(String login) {
			this.login = login;
		}

		public String getId() {
			return id;
		}

		public void setId(String id) {
			this.id = id;
		}

}
